const { ethers } = require("hardhat");

async function main() {
    const [deployer] = await ethers.getSigners();
  
    console.log("Deploying contracts with the account:", deployer.address);
   
    const token = await ethers.getContractFactory("SimpleSwap");
    const deployment = await token.deploy()
  
    console.log("Simple swap contract address:", await deployment.address);
  }
  
  main()
    .then(() => process.exit(0))
    .catch((error) => {
      console.error(error);
      process.exit(1);
    });